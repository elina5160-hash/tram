function splitPrice(s: string) {
  const m = s.match(/^(.*?—Ä—É–±\.?)/i)
  if (m) {
    const main = m[1].trim()
    let rest = s.slice(m[1].length).trim()
    if (rest.startsWith("/")) rest = rest.slice(1).trim()
    return { main, sub: rest }
  }
  const parts = s.split("/")
  return { main: (parts[0] || "").trim(), sub: (parts[1] || "").trim() }
}

